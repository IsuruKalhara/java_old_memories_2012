    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        if(!(option.equals(""))){
            try{
            if(jRadioButton4.isSelected()){
                r2 = Integer.parseInt(jT.getText(),2);
            }if(jRadioButton3.isSelected()){
                r2 = Integer.parseInt(jT.getText(),8);
            }if(jRadioButton1.isSelected()){
                r2 = Integer.parseInt(jT.getText(),16);
            }if(jRadioButton2.isSelected()){
                r2 = Double.parseDouble(jT.getText());
            }
            
            
            if(option.equals("-"))
            r1=r1-r2;
            if(option.equals("+"))
            r1=r1+r2;
            if(option.equals("*"))
            r1=r1*r2;
            if(option.equals("/"))
            r1=r1/r2;
            if(option.equals("^")){
                r1 = Math.pow(r1, r2);
            }
            if(option.equals("r")){
                r1 = Math.pow(r1,(1/r2));
            }
            if(option.equals("nPr")){
                r1 = (fac((int)r1))/(fac((int)r1-(int)r2));
            }
            if(option.equals("nCr")){
                r1 = (fac((int)r1))/((fac((int)r2))*(fac((int)r1-(int)r2)));
            }
            if(option.equals("mod")){
                r1 = r1%r2;
            }
            if(jRadioButton4.isSelected()){
                jT.setText(Integer.toBinaryString((int) r1));
            }if(jRadioButton3.isSelected()){
                jT.setText(Integer.toOctalString((int) r1));
            }if(jRadioButton1.isSelected()){
                jT.setText(Integer.toHexString((int) r1));
            }if(jRadioButton2.isSelected()){
                jT.setText(r1 + "");
            }
            option = "";
            option1 = "l";
            d=false;
        }catch(NumberFormatException e){
            jT.setText("Error.");
            option1 = "l";
            option = "";
            d=false;
        }catch(NullPointerException f){
            jT.setText("0");
            option1 = "l";
            option = "";
            d=false;
        }
        }
        if(option.equals("")){
            try{
                if(jRadioButton4.isSelected()){
                    r1 = Integer.parseInt(jT.getText(),2);
                }if(jRadioButton3.isSelected()){
                    r1 = Integer.parseInt(jT.getText(),8);
                }if(jRadioButton1.isSelected()){
                    r1 = Integer.parseInt(jT.getText(),16);
                }if(jRadioButton2.isSelected()){
                    r1 = Double.parseDouble(jT.getText());
                }

                option1 = "l";
                option ="+";
                d=false;
            }
            catch(NumberFormatException e){
                jT.setText("This value is incorrect");
                d=false;
                option1 ="l";
            }
        }   
    }//GEN-LAST:event_jButton13ActionPerformed

